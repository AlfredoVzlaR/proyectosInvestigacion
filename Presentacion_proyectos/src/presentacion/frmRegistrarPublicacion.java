    private void agregarRevista(){
        int secuencia = Integer.parseInt(this.txtSecuencia.getText());
        Publicaciones publicaciones = new Publicaciones (secuencia, this.txtTitulo.getText());
        EnRevista revista = new EnRevista(this.txtNombre.getText(), this.txtEditorialLugar.getText(), Integer.parseInt(this.txtVolumenPais.getText()),
        Integer.parseInt(this.txtNumeroEditorial.getText()),Integer.parseInt(this.txtPagInicio.getText()), Integer.parseInt(this.txtPagFin.getText()));
        publicaciones.setTipoPublicacion(revista.getId());
        publicaciones.setIdProyecto(proyecto.getId());
        publicaciones.setProfesores(profesoresParticipantes);
        
        FabricaLogica.getInstancia().registrarPublicacion(publicaciones, revista);
    }
    
    private void agregarCongreso(){
        int secuencia = Integer.parseInt(this.txtSecuencia.getText());
        Publicaciones publicaciones = new Publicaciones (secuencia, this.txtTitulo.getText());
        EnCongreso congreso;
        if(this.comboBoxTipo.getSelectedIndex() == 0){
            congreso = new EnCongreso(Tipo.Nacional, this.txtNombre.getText(),this.fechaInicio.getDate(), this.fechaFin.getDate(),
                   this.txtEditorialLugar.getText(), this.txtVolumenPais.getText(), this.txtNumeroEditorial.getText());
        } else {
             congreso = new EnCongreso(Tipo.Internacional, this.txtNombre.getText(),this.fechaInicio.getDate(), this.fechaFin.getDate(),
                   this.txtEditorialLugar.getText(), this.txtVolumenPais.getText(), this.txtNumeroEditorial.getText());
        }
        
        publicaciones.setTipoPublicacion(congreso.getId());
        FabricaLogica.getInstancia().registrarPublicacion(publicaciones, congreso);
    }
        
