    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (tipoCongreso.isSelected()) {this.txtPagInicio.setText("1"); this.txtPagFin.setText("2");}
        boolean verificarCampos = FabricaLogica.getInstancia().verificarInformacionAgregar(txtSecuencia.getText(), txtTitulo.getText(), profesoresParticipantes, txtNombre.getText(), txtEditorialLugar.getText(), txtVolumenPais.getText(), txtPagInicio.getText(), txtPagFin.getText());
        boolean verificarNumeric = FabricaLogica.getInstancia().verificarInformacionAgregar(this.txtSecuencia.getText(), tipoRevista.isSelected(), this.txtVolumenPais.getText(), this.txtNumeroEditorial.getText(), this.txtPagInicio.getText(), this.txtPagFin.getText(), this.txtNumeroEditorial.getText());
        boolean verificar = FabricaLogica.getInstancia().verificarInformacionAgregar(tipoCongreso.isSelected(), this.txtSecuencia.getText());
        boolean verificarPublicaciones = FabricaLogica.getInstancia().verificarPublicacion(txtTitulo.getText());
        if(modeloIntegrantes.isEmpty()){
            JOptionPane.showMessageDialog(this,"Se requiere de al menos un profesor","Advertencia",JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (tipoRevista.isSelected() || tipoCongreso.isSelected()) {

            if (/*!verificarCampos ||*/ !verificarNumeric || !verificar) {

                mostrarAdvertenciaCamposErroneos();
                return;
            }
            if (!verificarPublicaciones) {
                mostrarMsjErrorPublicacionExistente();
            } else {

                if (this.tipoRevista.isSelected()) {
                    this.agregarRevista();
                    limpiar();
                } else if (this.tipoCongreso.isSelected()) {
                    this.agregarCongreso();
                    limpiar();
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Es necesario seleccionar un tipo.", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
        
        limpiar();
        limpiarTipo();
    }//GEN-LAST:event_btnAgregarActionPerformed

